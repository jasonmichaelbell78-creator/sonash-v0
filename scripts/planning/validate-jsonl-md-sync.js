#!/usr/bin/env node
/**
 * validate-jsonl-md-sync.js
 *
 * Checks that generated MD views match their JSONL sources.
 * Backs up each MD, runs the generator (normal mode), compares output to backup,
 * then restores the original. Exits non-zero if any MD would change.
 *
 * Per D84/D85: Enforced at pre-commit when planning JSONL files are staged.
 *
 * Usage:
 *   node scripts/planning/validate-jsonl-md-sync.js
 */

import { execFileSync } from "node:child_process";
import { readFileSync, copyFileSync, existsSync, unlinkSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = resolve(__dirname, "..", "..");

const GENERATORS = [
  {
    script: "scripts/planning/generate-discovery-record.js",
    output: ".planning/system-wide-standardization/DISCOVERY_RECORD.md",
    name: "DISCOVERY_RECORD.md",
  },
  {
    script: "scripts/planning/generate-decisions.js",
    output: ".planning/system-wide-standardization/DECISIONS.md",
    name: "DECISIONS.md",
  },
];

let failures = 0;

// Normalize: strip per-run timestamps so comparison is stable
const normalize = (s) =>
  s
    .replace(/\*Generated by.*?\d{4}-\d{2}-\d{2}T[\d:.]+Z\*.*$/m, "[TIMESTAMP]")
    .replace(/\*\*Generated:\*\* \d{4}-\d{2}-\d{2}/m, "**Generated:** [DATE]")
    .replace(/ +$/gm, "")
    .trimEnd();

for (const gen of GENERATORS) {
  const outputPath = join(ROOT, gen.output);
  const backupPath = outputPath + ".bak";

  // Read current MD
  let currentMd;
  try {
    currentMd = readFileSync(outputPath, "utf-8");
  } catch {
    console.error(`  ❌ ${gen.name} does not exist — run: node ${gen.script}`);
    failures++;
    continue;
  }

  // Backup, regenerate, compare, restore
  try {
    copyFileSync(outputPath, backupPath);

    // Run generator in normal mode (writes to the actual output file)
    execFileSync("node", [join(ROOT, gen.script)], {
      encoding: "utf-8",
      cwd: ROOT,
      stdio: "pipe",
    });

    const freshMd = readFileSync(outputPath, "utf-8");

    // Restore original
    copyFileSync(backupPath, outputPath);

    // Compare
    if (normalize(currentMd) === normalize(freshMd)) {
      console.log(`  ✅ ${gen.name} is in sync`);
    } else {
      console.error(`  ❌ ${gen.name} is stale — regenerate with: node ${gen.script}`);
      failures++;
    }
  } catch (err) {
    // Restore on error
    if (existsSync(backupPath)) {
      copyFileSync(backupPath, outputPath);
    }
    console.error(`  ❌ ${gen.name} generator failed: ${err.message}`);
    failures++;
  } finally {
    // Clean up backup
    try {
      if (existsSync(backupPath)) {
        unlinkSync(backupPath);
      }
    } catch {
      // ignore cleanup failures
    }
  }
}

if (failures > 0) {
  console.error(`\n  ${failures} MD file(s) out of sync with JSONL sources.`);
  console.error("  Run the generator scripts above, stage the updated MD, then commit.");
  process.exit(1);
} else {
  console.log("  ✅ All MD views match JSONL sources");
}
